package itsfcp.isc.edu.Hourhand.entidades;

public class carreras {
    private String id_carrera;
    private String n_carrera;
    private String clave_carrera;

    public carreras() {
        this.id_carrera = id_carrera;
        this.n_carrera = n_carrera;
        this.clave_carrera = clave_carrera;
    }

    public String getId_carrera() {
        return id_carrera;
    }

    public void setId_carrera(String id_carrera) {
        this.id_carrera = id_carrera;
    }

    public String getN_carrera() {
        return n_carrera;
    }

    public void setN_carrera(String n_carrera) {
        this.n_carrera = n_carrera;
    }

    public String getClave_carrera() {
        return clave_carrera;
    }

    public void setClave_carrera(String clave_carrera) {
        this.clave_carrera = clave_carrera;
    }
}