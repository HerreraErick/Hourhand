package itsfcp.isc.edu.Hourhand.entidades;

public class semestre {
    private String id_semestre;
    private String n_semestre;

    public semestre() {
        this.id_semestre = id_semestre;
        this.n_semestre = n_semestre;
    }

    public String getId_semestre() {
        return id_semestre;
    }

    public void setId_semestre(String id_semestre) {
        this.id_semestre = id_semestre;
    }

    public String getN_semestre() {
        return n_semestre;
    }

    public void setN_semestre(String n_semestre) {
        this.n_semestre = n_semestre;
    }
}